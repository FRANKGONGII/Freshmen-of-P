def cheese_and_crackers(cheese_count, boxes_of_crackers):
    print("You have %d cheese" % cheese_count)
    print("You have %d boxes of crackers" % boxes_of_crackers)


cheese_and_crackers(20, 30)
amount_of_cheese = 10
